#!/usr/bin/env node
/**
 * T4c retire-migration generator (brief 2026-07-02-brief-t4c-retire-migration.md).
 *
 * Reads the user-adjudicated artifact docs/plans/t4-dedup/decisions.json (the
 * SINGLE source of truth: 61 lessons to retire across 57 groups, 250 survivors)
 * and emits, byte-for-byte deterministically:
 *   - supabase/migrations/<VERSION>_t4_dedup_retire.sql          (soft-retire + rollback snapshot)
 *   - supabase/migrations/<VERSION>_t4_dedup_retire.sql.rollback (restore from snapshot)
 *
 * NO lesson_id is ever hand-typed anywhere in the migration: everything below is
 * generated from the artifact. Re-running this script reproduces the exact files
 * (no Date.now / randomness — the only runtime timestamp is the SQL now() that
 * executes at apply time, not generation time).
 *
 * Usage:  node scripts/dedup-sweep/generate-retire-migration.mjs
 *         (writes the two files; pass --stdout to print the .sql instead)
 *
 * This script is READ-ONLY w.r.t. the database (it never connects to Supabase),
 * so it carries no prod-guard — it only reads a repo JSON file and writes SQL.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

// Full-timestamp version that sorts AFTER 20260702150000_lock_down_invitation_token_access.sql
// (bare-date YYYYMMDD_ names sort AFTER same-day full timestamps — ASCII gotcha — so never use one).
const VERSION = '20260702160000';
const MIGRATION_BASENAME = `${VERSION}_t4_dedup_retire`;

const root = resolve(dirname(fileURLToPath(import.meta.url)), '../..');
const decisionsPath = resolve(root, 'docs/plans/t4-dedup/decisions.json');
const sqlPath = resolve(root, `supabase/migrations/${MIGRATION_BASENAME}.sql`);
const rollbackPath = resolve(root, `supabase/migrations/${MIGRATION_BASENAME}.sql.rollback`);

const decisions = JSON.parse(readFileSync(decisionsPath, 'utf8'));

// --- Extract from the artifact (order-stable: group order, then array order) ---
const retiredPairs = []; // { group_id, lesson_id, title }
const survivorIds = [];
for (const g of decisions.groups) {
  for (const r of g.retired ?? []) retiredPairs.push({ group_id: g.group_id, lesson_id: r.lesson_id, title: r.title });
  for (const s of g.survivors ?? []) survivorIds.push(s.lesson_id);
}

// --- Integrity gate (mirrors the brief's STOP conditions; abort emit on any failure) ---
const ID_RE = /^[A-Za-z0-9_-]+$/;
const SLUG_RE = /^[a-z0-9-]+$/;
const fail = (msg) => {
  console.error(`generate-retire-migration: ABORT — ${msg}`);
  process.exit(1);
};

const retiredIds = retiredPairs.map((p) => p.lesson_id);
const retiredSet = new Set(retiredIds);
const survivorSet = new Set(survivorIds);

if (retiredPairs.length !== 61) fail(`expected 61 retired rows, got ${retiredPairs.length}`);
if (retiredSet.size !== 61) fail(`retired lesson_ids not distinct: ${retiredSet.size} unique of ${retiredPairs.length}`);
if (survivorSet.size !== survivorIds.length) fail(`survivor lesson_ids not distinct: ${survivorSet.size} unique of ${survivorIds.length}`);
if (survivorSet.size !== 250) fail(`expected 250 distinct survivors, got ${survivorSet.size}`);
for (const id of retiredSet) if (survivorSet.has(id)) fail(`lesson_id in BOTH retired and survivors: ${id}`);
for (const p of retiredPairs) {
  if (!ID_RE.test(p.lesson_id)) fail(`unsafe lesson_id: ${JSON.stringify(p.lesson_id)}`);
  if (!SLUG_RE.test(p.group_id)) fail(`unsafe group_id (retired_reason would be dedup:${p.group_id}): ${JSON.stringify(p.group_id)}`);
}
for (const id of survivorIds) if (!ID_RE.test(id)) fail(`unsafe survivor lesson_id: ${JSON.stringify(id)}`);

const retiringGroups = new Set(retiredPairs.map((p) => p.group_id)).size;

// --- SQL literal builders (ids/groups are charset-validated above → quote-safe) ---
const q = (s) => `'${s}'`;
const idArray = (ids, indent) =>
  `ARRAY[\n${ids.map((id) => `${indent}  ${q(id)}`).join(',\n')}\n${indent}]::text[]`;
const pairsValues = (indent) =>
  retiredPairs.map((p) => `${indent}(${q(p.group_id)}, ${q(p.lesson_id)})`).join(',\n');

// --- Emit .sql ---
const sql = `-- =====================================================
-- Migration: ${MIGRATION_BASENAME}.sql
-- =====================================================
-- T4c — soft-retire ${retiredPairs.length} user-approved duplicate lessons (go-live sprint).
--
-- Source of truth: docs/plans/t4-dedup/decisions.json (the T4 walkthrough
-- artifact — every verdict user-confirmed live 2026-07-02). This file is
-- GENERATED by scripts/dedup-sweep/generate-retire-migration.mjs; DO NOT
-- hand-edit — re-run the generator to regenerate. No lesson_id is hand-typed.
--
-- ${retiredPairs.length} lessons across ${retiringGroups} groups (39 retire_duplicate + 18 keep_family
-- with an embedded exact-copy pair). ${survivorSet.size} named survivors stay live.
--
-- Mechanism (design decision D5): SOFT-retire only — never DELETE. Sets
-- retired_at = now() and retired_reason = 'dedup:<group_id>' on the losers.
-- The retired_at / retired_reason columns already exist (added by the imports
-- cleanup, 20260520000000). search_lessons + the search view already filter
-- retired_at IS NULL, so retired rows drop out of public search but stay
-- linkable by direct id via lessons_with_metadata (20260520030000). Survivors
-- are untouched — no metadata blending/merging.
--
-- ATOMICITY (structurally modeled on the GUARDED precedent c02_retag_apply,
-- 20260625000000 — NOT the un-guarded imports cleanup): this repo's supabase
-- CLI applies migration statements in AUTOCOMMIT (extended protocol), so
-- without an explicit BEGIN/COMMIT the section-(5) UPDATE would commit before
-- the section-(6) post-asserts run, and a failing assert could NOT roll the
-- write back (c02_retag_apply:59-64 documents this first-hand). So the whole
-- body runs in ONE explicit transaction, and a SHARE ROW EXCLUSIVE lock on
-- lessons serializes the apply against concurrent reviewer edits — so the
-- guards see one consistent snapshot and every assert can abort atomically.
--
-- Rollback: sibling ${MIGRATION_BASENAME}.sql.rollback restores retired_at /
-- retired_reason for exactly these ${retiredPairs.length} ids from the snapshot table below.
-- The snapshot table public.t4_dedup_retire_rollback is LEFT IN PLACE as the
-- recovery artifact (a post-launch cleanup migration drops it once PROD-stable).
--
-- PROD pre-registered state (read-only probe 2026-07-02): live corpus 764,
-- all 61 present + live + none already retired → post-apply live corpus 703.
-- TEST may hold fewer than 61 of these ids; the in-migration asserts below are
-- written to hold for "all targets present in THIS db", not a hard 61 — the
-- exact-61 check is a PROD-only environment verification (see the brief).

-- The whole apply runs in ONE explicit transaction (see ATOMICITY above).
BEGIN;

-- Serialize against concurrent writes to public.lessons for the (sub-second)
-- apply, so the snapshot (3), pre-guard (4), UPDATE (5) and post-asserts (6)
-- all see ONE consistent snapshot. SHARE ROW EXCLUSIVE blocks concurrent
-- INSERT/UPDATE/DELETE but still allows SELECT; released at COMMIT. (Requires
-- the surrounding transaction — a bare LOCK errors in autocommit.) Mirrors
-- c02_retag_apply:78.
LOCK TABLE public.lessons IN SHARE ROW EXCLUSIVE MODE;

-- =====================================================
-- (1) Rollback snapshot table (service-role only: RLS on, NO policies —
--     mirrors the c02_retag_rollback / wave4 snapshot precedent).
-- =====================================================
CREATE TABLE IF NOT EXISTS public.t4_dedup_retire_rollback (
  lesson_id            text PRIMARY KEY,
  prior_retired_at     timestamptz,
  prior_retired_reason text,
  group_id             text NOT NULL,
  snapshotted_at       timestamptz NOT NULL DEFAULT now()
);
ALTER TABLE public.t4_dedup_retire_rollback ENABLE ROW LEVEL SECURITY;

-- =====================================================
-- (2) Compile-time guard — the embedded target list is exactly ${retiredPairs.length} distinct ids.
--     (A property of THIS file, independent of DB contents.)
-- =====================================================
DO $$
DECLARE
  n_total    int;
  n_distinct int;
BEGIN
  SELECT count(*), count(DISTINCT id)
    INTO n_total, n_distinct
  FROM unnest(${idArray(retiredIds, '    ')}) AS id;
  IF n_total <> ${retiredPairs.length} OR n_distinct <> ${retiredPairs.length} THEN
    RAISE EXCEPTION 't4c ABORT: embedded target list is % rows / % distinct (expected ${retiredPairs.length}/${retiredPairs.length})',
      n_total, n_distinct;
  END IF;
END $$;

-- =====================================================
-- (3) Snapshot the CURRENT state of every present target BEFORE the update.
--     INNER JOIN → only rows that actually exist in THIS db are snapshotted
--     (matches the precedent; on TEST that may be < ${retiredPairs.length}).
-- =====================================================
INSERT INTO public.t4_dedup_retire_rollback (lesson_id, prior_retired_at, prior_retired_reason, group_id)
SELECT v.lesson_id, l.retired_at, l.retired_reason, v.group_id
FROM (VALUES
${pairsValues('  ')}
) AS v(group_id, lesson_id)
JOIN public.lessons l ON l.lesson_id = v.lesson_id
ON CONFLICT (lesson_id) DO NOTHING;

-- =====================================================
-- (4) Pre-update guard — refuse if ANY present target is already retired.
--     Reads LIVE lessons.retired_at (via the snapshot join), so it catches a
--     double-apply or drift regardless of what the snapshot captured. Aborting
--     here rolls back the whole migration transaction (snapshot included).
-- =====================================================
DO $$
DECLARE
  n_already int;
BEGIN
  SELECT count(*)
    INTO n_already
  FROM public.lessons l
  JOIN public.t4_dedup_retire_rollback r ON r.lesson_id = l.lesson_id
  WHERE l.retired_at IS NOT NULL;
  IF n_already <> 0 THEN
    RAISE EXCEPTION 't4c ABORT: % target row(s) already retired — refusing to overwrite retired_reason (double-apply or corpus drift)',
      n_already;
  END IF;
END $$;

-- =====================================================
-- (5) Soft-retire the losers. The retired_at IS NULL guard makes this a no-op
--     for any already-retired row (belt-and-suspenders with guard 4).
-- =====================================================
UPDATE public.lessons l
SET retired_at = now(),
    retired_reason = 'dedup:' || r.group_id
FROM public.t4_dedup_retire_rollback r
WHERE l.lesson_id = r.lesson_id
  AND l.retired_at IS NULL;

-- =====================================================
-- (6) Post-update asserts (hard failures):
--     - every present target is now retired with a dedup:% reason
--     - snapshot row count matches present-target count
--     - ZERO survivors carry a dedup:% reason (survivors must never be retired)
-- =====================================================
DO $$
DECLARE
  n_present         int;
  n_retired_dedup   int;
  n_snapshot        int;
  n_survivor_dedup  int;
BEGIN
  SELECT count(*) INTO n_present
  FROM public.lessons l
  JOIN public.t4_dedup_retire_rollback r ON r.lesson_id = l.lesson_id;

  SELECT count(*) INTO n_retired_dedup
  FROM public.lessons l
  JOIN public.t4_dedup_retire_rollback r ON r.lesson_id = l.lesson_id
  WHERE l.retired_at IS NOT NULL AND l.retired_reason LIKE 'dedup:%';

  SELECT count(*) INTO n_snapshot FROM public.t4_dedup_retire_rollback;

  IF n_retired_dedup <> n_present THEN
    RAISE EXCEPTION 't4c ABORT: % of % present targets retired with dedup:%% (expected all present targets retired)',
      n_retired_dedup, n_present;
  END IF;
  IF n_snapshot <> n_present THEN
    RAISE EXCEPTION 't4c ABORT: snapshot has % rows but % targets present in this db',
      n_snapshot, n_present;
  END IF;

  SELECT count(*) INTO n_survivor_dedup
  FROM public.lessons l
  WHERE l.lesson_id = ANY(${idArray(survivorIds, '    ')})
    AND l.retired_reason LIKE 'dedup:%';
  IF n_survivor_dedup <> 0 THEN
    RAISE EXCEPTION 't4c ABORT: % survivor row(s) carry a dedup:%% retired_reason — survivors must never be retired',
      n_survivor_dedup;
  END IF;

  RAISE NOTICE 't4c dedup retire OK: % present target(s) retired (snapshot=%), 0 survivors touched',
    n_retired_dedup, n_snapshot;
END $$;

-- Commit the whole apply atomically (releases the lesson lock). If any statement
-- above raised, this transaction rolls back every write — snapshot INSERT and
-- UPDATE included.
COMMIT;

-- =====================================================
-- ROLLBACK — see sibling ${MIGRATION_BASENAME}.sql.rollback
-- =====================================================
-- UPDATE public.lessons l
-- SET retired_at = r.prior_retired_at, retired_reason = r.prior_retired_reason
-- FROM public.t4_dedup_retire_rollback r
-- WHERE l.lesson_id = r.lesson_id;
`;

// --- Emit .rollback ---
const rollback = `-- =====================================================
-- ROLLBACK: ${MIGRATION_BASENAME}.sql.rollback
-- =====================================================
-- Un-retires exactly the ${retiredPairs.length} lessons soft-retired by
-- ${MIGRATION_BASENAME}.sql, restoring their prior retired_at / retired_reason
-- from the snapshot table public.t4_dedup_retire_rollback. Since all ${retiredPairs.length}
-- were live at apply time, prior_retired_at / prior_retired_reason are NULL, so
-- this returns them to the live corpus. Touches ONLY rows named in the snapshot.
--
-- The snapshot table is LEFT IN PLACE (recovery artifact); a future cleanup
-- migration drops it once PROD-stable. Generated by
-- scripts/dedup-sweep/generate-retire-migration.mjs — do not hand-edit.

-- The AND r.lesson_id = ANY(...) clause restricts the restore to exactly the
-- ${retiredPairs.length} t4c targets even if the snapshot table were ever polluted with
-- non-target rows (defense-in-depth; on the real apply the table holds only these).
UPDATE public.lessons l
SET retired_at     = r.prior_retired_at,
    retired_reason = r.prior_retired_reason
FROM public.t4_dedup_retire_rollback r
WHERE l.lesson_id = r.lesson_id
  AND r.lesson_id = ANY(${idArray(retiredIds, '  ')});
`;

if (process.argv.includes('--stdout')) {
  process.stdout.write(sql);
} else {
  writeFileSync(sqlPath, sql);
  writeFileSync(rollbackPath, rollback);
  console.log(
    `Wrote:\n  ${sqlPath}\n  ${rollbackPath}\n` +
      `${retiredPairs.length} targets across ${retiringGroups} groups; ${survivorSet.size} survivors. Integrity gate: PASS.`
  );
}
